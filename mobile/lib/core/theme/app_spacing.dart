/// Spacing scale (logical pixels). Use these instead of raw numbers so vertical
/// rhythm and padding stay consistent across every screen.
class AppSpacing {
  AppSpacing._();

  static const double xs = 4;
  static const double sm = 8;
  static const double md = 12;
  static const double lg = 16;
  static const double xl = 20;
  static const double xxl = 24;
  static const double xxxl = 32;

  /// Standard horizontal page gutter for full-screen content.
  static const double page = 24;
}
